// 불린 ( Boolean )

//숫자
console.log(2 > 1);
console.log(2 < 1);
console.log(3 >= 2);
console.log(3 <= 3);
console.log(3 === 3);
console.log(3 !== 3);

//문자열
console.log('Codeit' === 'Codeit'); //True
console.log('Codeit' !== 'Codeit'); //False

// ===과 ==의 차이
console.log(1 == '1');  //True  -- 값만 비교
console.log(1 === '1'); //False -- 값 + 자료형 비교